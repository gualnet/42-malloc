int		ft_sqrt(int nb)
{
	int a;

	a = 1;
	if ((nb > 0) && (nb < 2147395601))
	{
		while (a < 46341)
		{
			if ((a * a) == nb)
				return (a);
			a++;
		}
	}
	return (0);
}
