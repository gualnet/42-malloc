#include "ft_printf.h"

int	ft_putwstr(wchar_t *str)
{
	int		i;
	int		ret;

	ret = 0;
	i = 0;
	while (str[i] != '\0')
	{
		ret += ft_putwchar(str[i]);
		i++;
	}
	return (ret);
}
