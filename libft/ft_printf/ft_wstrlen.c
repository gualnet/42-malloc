#include "ft_printf.h"

int		ft_wstrlen(wchar_t *wstr)
{
	int	i;
	int	len;

	len = 0;
	i = 0;
	while (wstr[i] != '\0')
	{
		len += ft_wcharlen(wstr[i]);
		i++;
	}
	return (len);
}
